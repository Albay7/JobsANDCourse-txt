                <li>Mid-Level Engineers (3-5 years): PHP 70,000 - PHP 120,000 per month</li>
                <li>Senior Engineers (5+ years): PHP 120,000 - PHP 200,000+ per month</li>
                <li>AI Engineers in high-demand industries, such as finance or healthcare, or in cities like Metro Manila, often command higher salaries.</li>
