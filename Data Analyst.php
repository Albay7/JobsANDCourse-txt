            <h2>Duties and Responsibilities</h2>
            <p>Data Analysts focus on transforming raw data into meaningful insights. Key responsibilities include:</p>
            <ul>
                <li>Collecting and cleaning datasets to ensure accuracy.</li>
                <li>Analyzing data using statistical methods and tools.</li>
                <li>Collaborating with teams to identify and solve business problems.</li>
                <li>Creating reports and dashboards to visualize data trends.</li>
                <li>Providing actionable recommendations based on data insights.</li>
            </ul>
