            <p>The Bachelor of Science in Computer Engineering combines principles of electrical engineering and computer science to prepare students to design, develop, and maintain computer systems and hardware. It is designed for students interested in the design and optimization of both hardware and software systems, with an emphasis on practical applications of engineering principles.</p>

